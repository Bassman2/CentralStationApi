using GeneratorLibrary;
using Microsoft.CodeAnalysis;
using System.Linq;
using System.Text;

namespace CsSerializerGenerator
{
    [Generator]
    public class SerializeGenerator : Generator
    {
        public override void Excecute()
        {
            //Debugger.Launch();

            // for debug only
            CreateDebug();

            // get all classes with [CsSerializeAttribute] 
            foreach (var cl in GetAllClassesWithAttribute("CentralStationApi.Serializer.CsSerializeAttribute"))
            {
                CreateSerializerClassFile(cl);
                //CreateModelFile(cl);
                //CreateCastFile(cl);
            }

            // get all enums with [EnumConverterAttribute] 
            foreach (var en in GetAllEnumsWithAttribute("CentralStationApi.Serializer.EnumConverterAttribute"))
            {
                CreateSerializerEnumFile(en);
                //CreateModelFile(cl);
                //CreateCastFile(cl);
            }
        }

        private void CreateSerializerClassFile(Class cl)
        {
            StringBuilder sb = new(
                $$"""
                // <auto-generated />

                #nullable enable annotations
                #nullable disable warnings

                // Suppress warnings about [Obsolete] member usage in generated code.
                #pragma warning disable CS0612, CS0618

                namespace {{cl.NameSpace}};

                partial class {{cl.Name}} : ICsSerialize
                {
                    /// <summary>
                    /// Handles the completion of deserializing a nested section within an article.
                    /// </summary>
                    /// <param name="name">The name of the section that has been deserialized.</param>
                    /// <returns>
                    /// An <see cref="ICsSerialize"/> implementation for the nested section, 
                    /// or a <see cref="CsDummy"/> instance if the section name is unknown.
                    /// </returns>
                    public ICsSerialize? DeserializeLeave(string name)
                    {
                        switch (name)
                        {

                """);

            AddLeaves(sb, cl);

            sb.AppendLine(
                $$"""
                        default:
                            Debug.WriteLine($"Unknown {{cl.Name}} section {name}");
                            return new CsDummy(name);
                        }
                    }

                    /// <summary>
                    /// Deserializes a property value and assigns it to the corresponding article property.
                    /// </summary>
                    /// <param name="name">The name of the property as it appears in the serialized data.</param>
                    /// <param name="value">The string value to deserialize and assign to the property.</param>
                    public void DeserializeProperty(string name, string value)
                    {
                        switch (name)
                        {
                """);

            AddProperties(sb, cl);

            sb.AppendLine(
                $$"""
                        default:
                            Debug.WriteLine($"Unknown {{cl.Name}} property {name}");
                            break;
                        }
                    }
                }
                """);

            AddSource($"{cl.Name}.g.cs", sb.ToString());
        }

        private void AddLeaves(StringBuilder sb, Class cl)
        {
            foreach (var prop in cl.Properties.Where(p => p.HasAttribute("CentralStationApi.Serializer.CsPropertyAttribute")))
            {
                var arg = prop.GetAttribute("CentralStationApi.Serializer.CsPropertyAttribute")?.ConstructorArguments.First();
                if (arg == null) continue;

                //sb.AppendLine($"// {prop.Name} - {prop.Type.Name} - {prop.Type.FullName} - {prop.Type.TypeKind} || {prop.Type.BaseTypeName} - {prop.Type.BaseTypeFullName}");

                if (prop.Type.TypeKind == TypeKind.Class)
                {
                    if (prop.Type.Name == "List")
                    {
                        sb.AppendLine($"        case \"{arg.Value}\":");
                        sb.AppendLine($"            {prop.Name} ??= [];");
                        sb.AppendLine($"            var {LowercaseFirst(prop.Name)} = new {prop.Type.BaseTypeName}();");
                        sb.AppendLine($"            {prop.Name}.Add({LowercaseFirst(prop.Name)});");
                        sb.AppendLine($"            return {LowercaseFirst(prop.Name)};");
                    }
                    //else if (prop.Type.Name == "Version")
                    //{
                    //    sb.AppendLine($"        case \"{arg.Value}\":");
                    //    sb.AppendLine($"            return {prop.Name} = (Version)new CsVersion();");
                    //}
                    else if (prop.Type.FullName == "string" || prop.Type.FullName == "string?")
                    {
                        // do nothing
                    }
                    else
                    {
                        sb.AppendLine($"        case \"{arg.Value}\":");
                        sb.AppendLine($"            return {prop.Name} = new {prop.Type.Name}();");
                    }
                }
            }
        }

        private void AddProperties(StringBuilder sb, Class cl)
        {
            foreach (var prop in cl.Properties.Where(p => p.HasAttribute("CentralStationApi.Serializer.CsPropertyAttribute")))
            {
                var arg = prop.GetAttribute("CentralStationApi.Serializer.CsPropertyAttribute")?.ConstructorArguments.First();
                if (arg == null) continue;

                switch (prop.Type.FullName)
                {
                case "List":
                    //sb.AppendLine($"// List {prop.Name} {prop.Type.FullName}");
                    break;
                case "string?":
                    sb.AppendLine($"        case \"{arg.Value}\":");
                    sb.AppendLine($"            {prop.Name} = value;");
                    sb.AppendLine("            break;");
                    break;
                case "int":
                    sb.AppendLine($"        case \"{arg.Value}\":");
                    sb.AppendLine($"            {prop.Name} = CsSerializer.ToInt(value);");
                    sb.AppendLine("            break;");
                    break;
                case "uint":
                    sb.AppendLine($"        case \"{arg.Value}\":");
                    sb.AppendLine($"            {prop.Name} = CsSerializer.ToUInt(value);");
                    sb.AppendLine("            break;");
                    break;
                case "short":
                    sb.AppendLine($"        case \"{arg.Value}\":");
                    sb.AppendLine($"            {prop.Name} = CsSerializer.ToShort(value);");
                    sb.AppendLine("            break;");
                    break;
                case "ushort":
                    sb.AppendLine($"        case \"{arg.Value}\":");
                    sb.AppendLine($"            {prop.Name} = CsSerializer.ToUShort(value);");
                    sb.AppendLine("            break;");
                    break;
                case "List<int>":
                    sb.AppendLine($"        case \"{arg.Value}\":");
                    sb.AppendLine($"            {prop.Name} = CsSerializer.ToIntArray(value);");
                    sb.AppendLine("            break;");
                    break;
                case "List<uint>":
                    sb.AppendLine($"        case \"{arg.Value}\":");
                    sb.AppendLine($"            {prop.Name} = CsSerializer.ToUIntArray(value);");
                    sb.AppendLine("            break;");
                    break;
                case "int[]?":
                    sb.AppendLine($"        case \"{arg.Value}\":");
                    sb.AppendLine($"            {prop.Name} = CsSerializer.ToIntArray(value);");
                    sb.AppendLine("            break;");
                    break;
                case "uint[]?":
                    sb.AppendLine($"        case \"{arg.Value}\":");
                    sb.AppendLine($"            {prop.Name} = CsSerializer.ToUIntArray(value);");
                    sb.AppendLine("            break;");
                    break;
                case "bool":
                    sb.AppendLine($"        case \"{arg.Value}\":");
                    sb.AppendLine($"            {prop.Name} = CsSerializer.ToBool(value);");
                    sb.AppendLine("            break;");
                    break;
                
                default:
                    if (prop.Type.IsEnum)
                    {
                        sb.AppendLine($"        case \"{arg.Value}\":");
                        sb.AppendLine($"            {prop.Name} = {prop.Type.Name}_Converter.Deserialize(value);");
                        sb.AppendLine("            break;");
                    }
                    else
                    {
                        sb.AppendLine($"// {prop.Name} - {prop.Type.Name} - {prop.Type.FullName} - {prop.Type.TypeKind} not defined");
                    }
                    break;
                }                   
                    
            }
        }

        private void CreateSerializerEnumFile(Enum en)
        {
            StringBuilder sb = new(
                $$"""
                // <auto-generated />

                #nullable enable annotations
                #nullable disable warnings

                // Suppress warnings about [Obsolete] member usage in generated code.
                #pragma warning disable CS0612, CS0618

                namespace {{en.NameSpace}};

                internal static class {{en.Name}}_Converter 
                {
                    public static {{en.Name}} Deserialize(string value)
                    {
                        if (int.TryParse(value, out int val))
                        {
                            return ({{en.Name}})val;
                        }
                        return value switch
                        {

                """);

            AddEnumField(sb, en);

            sb.AppendLine(
                $$"""
                        _ => ({{en.Name}})0
                        };
                    }
                }
                """);

            AddSource($"{en.Name}.g.cs", sb.ToString());
        }

        private void AddEnumField(StringBuilder sb, Enum en)
        {
            
            foreach (var field in en.Fields)
            {
                //foreach (var a in field.Attributes)
                //{
                //    sb.AppendLine($"// xx {a.Name}");
                //}
                var attr = field.GetAttribute("System.Runtime.Serialization.EnumMemberAttribute");
                //sb.AppendLine($"// {attr?.Name ?? "0"}");
                var arg = attr?.GetNamedArgument("Value");
                string name = arg?.Value ?? field.Name;
                sb.AppendLine($"        \"{name}\" => {en.Name}.{field.Name},");
            }
        }

        private string LowercaseFirst(string input)
        {
            if (string.IsNullOrEmpty(input) || char.IsLower(input[0]))
                return input;
            return char.ToLower(input[0]) + input.Substring(1);
        }
    }
}

    
